namespace HCILibrary.Enums;

/// <summary>
/// Key state enumeration for locally assigned keys (bits 6-7).
/// </summary>
public enum LocalKeyState : byte
{
    /// <summary>
    /// Key state 0.
    /// </summary>
    State0 = 0,

    /// <summary>
    /// Key state 1.
    /// </summary>
    State1 = 1,

    /// <summary>
    /// Key state 2.
    /// </summary>
    State2 = 2,

    /// <summary>
    /// Key state 3.
    /// </summary>
    State3 = 3
}
